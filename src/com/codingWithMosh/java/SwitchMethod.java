package com.codingWithMosh.java;

public class SwitchMethod {
	public static void main(String[] args) {
	
		int inumber = 200;
		
		switch (inumber) {
		case 1 : System.out.println("thsis id first case");
			break;
		case 200 : System.out.println("this is case 2");
			break;
		case 3: System.out.println(200);
			break;
		case 20: System.out.println("case is 200");
			break;
		default : System.out.println("default");
			
			
		
		}
		
		

	}
	

}
